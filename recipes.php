                                        <div class="sub-title">Method</div>

                                        <p>Heat oven to 180C/160C fan/gas 4. Grease 2 x 20cm round sandwich tins and line the bases with baking parchment. Tip all the sponge ingredients, apart from the food coloring, into a mixing bowl, then beat with an electric whisk until smooth.</p>

                                        <p>Working quickly, weigh the mixture into another bowl to work out the total weight, then weigh exactly half the mixture back into the mixing bowl. Pick 2 of your colors and stir a little into each mix. Keep going until you are happy with the color – the color of the batter now will be very similar to the finished cake, so be brave! Scrape the different batters into the tins, trying to spread and smooth as much as possible – but try not to waste a drop of the batter – a rubber spatula will help you. Bake on the same oven shelf for 12 minutes until a skewer poked into the middle comes out clean.</p>

                                        <p>Gently turn the cakes out onto a wire rack to cool. Wash the tins and bowls thoroughly, and start again from step 1 – this time using another 2 colors. Unless you’re making the optional pink 	layer, repeat one more time to get 6 sponges, all of different colors. Leave them all to cool.</p>

